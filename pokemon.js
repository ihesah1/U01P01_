let pokemonnotes = [];
let keysPokemon = "pokemonLocalSearchValues";

 ///////////// store  in localStorage
 
   function storetoLocalStorage(self) {
    let id = self.id;
    document.getElementById(id).style.color = "RED";
    let getjsonpokemonnotes = localStorage.getItem(keysPokemon);
    if (getjsonpokemonnotes == null) {

        let note = {};
        note.id = id;
   
        pokemonnotes.push(note);
        localStorage.setItem(keysPokemon, JSON.stringify(pokemonnotes));
    } 
    else {
        pokemonnotes = JSON.parse(getjsonpokemonnotes);
        for (i = 0; i < pokemonnotes.length; i++) {
            pokemonnotes[i].id = pokemonnotes[i].id;
        }
        let note = {};
        note.id = id;

        pokemonnotes.push(note);
        let updatedjsonpokemonnotes = JSON.stringify(pokemonnotes);
        localStorage.setItem(keysPokemon, updatedjsonpokemonnotes);
    }
}
